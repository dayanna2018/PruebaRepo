def suma(num_uno, num_dos):
    "Regresa un número entero el cual es un resultado de una suma """
    return num_uno + num_dos
def resta (num_uno, num_2):
    "Regresa un número entero el cual es un resultado de una resta"""
    return num_uno - num_2

def multiplicacion (num_uno, num_2):
    "Regresa un número entero el cual es un resultado de una multiplicacion"""
    return num_uno * num_2

def division (num_uno, num_2):
    "Regresa un número entero el cual es un resultado de una division"
    return num_uno / num_2


